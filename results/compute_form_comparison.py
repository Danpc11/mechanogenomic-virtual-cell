"""
compute_form_comparison.py — cache the functional-form R^2 comparison.

Runs the stochastic motor-clutch simulator once, fits the candidate functional
forms (linear / power / log / saturating) with symbolic.compare_physical_forms,
and writes the resulting R^2 values to results/form_comparison.json.

figure2() in make_figures.py reads that JSON, so the Fig 2c panel shows numbers
derived from the actual model rather than hardcoded constants. Re-run this
whenever the calibration or the motor-clutch model changes:

    python results/compute_form_comparison.py
"""
import sys
import json
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "src"))

import symbolic as sym


def main(n_points=30, reps=8, seed=0):
    E, sigma = sym.generate_stress_data(n_points=n_points, reps=reps)
    forms = sym.compare_physical_forms(E, sigma)
    # normalize to {short_name: r2}
    key_map = {
        "linear (aE+b)": "linear",
        "power (a·E^b)": "power",
        "log (a·lnE+b)": "log",
        "saturating (Vmax·E/(K+E))": "saturating",
    }
    r2 = {}
    for k, v in forms.items():
        val = v[0] if isinstance(v, tuple) else v
        r2[key_map.get(k, k)] = round(float(val), 3)

    out = dict(r2=r2, n_points=n_points, reps=reps,
               order=["linear", "power", "log", "saturating"],
               note="R2 of each functional form fit to motor-clutch sigma(E); "
                    "generated by results/compute_form_comparison.py")
    dest = Path(__file__).resolve().parent / "form_comparison.json"
    dest.write_text(json.dumps(out, indent=2))
    print(f"Wrote {dest}")
    for f in out["order"]:
        print(f"  {f:12s} R2 = {r2[f]:.3f}")
    return out


if __name__ == "__main__":
    main()
